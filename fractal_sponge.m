% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
cla
N_=get(handles.edit1,'string');
N=str2double(N_);
if N_>0
    set(handles.text2,'string','Valid input.鼠标拖动看看','ForegroundColor','Black');
    rotate3d on
    color1=get(handles.popupmenu1,'value');
    color1_=get(handles.popupmenu1,'string');
    colorn=cellstr(color1_);
    val_R=str2double(get(handles.edit2,'string'));
    val_G=str2double(get(handles.edit3,'string'));
    val_B=str2double(get(handles.edit4,'string'));
    val_col=[val_R val_G val_B];
    if color1==1&(isnan(val_R)||isnan(val_G)||isnan(val_B))
        set(handles.text2,'string','输入正确的RGB数值，如[0.1 1 0]','ForegroundColor','Red');
        val_col=[1 1 1];
    else
        set(handles.text7,'string',['使用',colorn{color1},'色！']);
    end
    switch color1
        case 1
            color_1=val_col;
        case 2
            color_1=[1 0 0];
        case 3
            color_1=[1 1 0];
        case 4
            color_1=[0 1 0];
        case 5
            color_1=[0 1 1];
        case 6
            color_1=[0 0 1];
        case 7
            color_1=[1 0 1];
    end
    fractal_3D(N,color_1)
else
    set(handles.text2,'string','Invalid input! Please type a positive number!','ForegroundColor','Red');
end


